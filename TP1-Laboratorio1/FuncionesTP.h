#ifndef FUNCIONESTP_H_INCLUDED
#define FUNCIONESTP_H_INCLUDED

/** \brief suma 2 variables
 *
 * \param la primera variable
 * \param la segunda variable
 * \return el resultado de la operacion
 *
 */
float sumar(float, float);

/** \brief resta 2 variables
 *
 * \param la primera variable
 * \param la segunda variable
 * \return el resultado de la operacion
 *
 */
float restar(float, float);

/** \brief multiplica 2 variables
 *
 * \param la primera variable
 * \param la segunda variable
 * \return el resultado de la operacion
 *
 */
float multiplicar(float, float);

/** \brief divide 2 variables
 *
 * \param la primera variable
 * \param la segunda variable
 * \return el resultado de la operacion
 *
 */
float dividir(float, float);

/** \brief calcula el factorial de una variable
 *
 * \param la variable
 * \return el resultado de la operacion
 *
 */
long int factorial(float);

#endif
