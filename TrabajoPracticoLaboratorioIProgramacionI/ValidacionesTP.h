#ifndef VALIDACIONESTP_H_INCLUDED
#define VALIDACIONESTP_H_INCLUDED

/** \brief verifica que el dato ingresado sea un numero flotante
 *
 * \param la variable con el dato a verificar
 * \return 1 si no es flotante. 0 si es flotante
 *
 */
float validarFlotante(float numero);

/** \brief verifica que el dato ingresado sea un numero entero
 *
 * \param la variable con el dato a verificar
 * \return 1 si no es entero. 0 si es entero
 *
 */
int validarNumero(int numero);

#endif
